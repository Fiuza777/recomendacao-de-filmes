recomendar_por_genero(Genero, Filme) :-
    genero(Filme, Genero).

recomendar_por_diretor(Diretor, Filme) :-
    diretor(Filme, Diretor).

recomendar_por_ator(Ator, Filme) :-
    ator(Filme, Ator).

recomendar_filme(Genero, Diretor, Ator, Filme) :-
    filme(Filme, Genero, Diretor, Atores),
    member(Ator, Atores).

iniciar :-
    writeln('Bem-vindo ao Sistema de Recomendacao de Filmes!'),
    writeln('Por favor, insira suas preferencias abaixo:'),

    writeln('Genero preferido : '),
    read(Genero),

    writeln('Diretor preferido : '),
    read(Diretor),

    writeln('Ator preferido : '),
    read(Ator),

    writeln('Buscando por filmes...'),
    (   recomendar_filme(Genero, Diretor, Ator, Filme) ->
        format('Nos recomendamos o filme: ~w~n', [Filme])
    ;   writeln('Desculpe, nao encontramos nenhum filme que atenda as suas preferencias.')
    ).